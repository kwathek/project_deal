        TestCin testc = new TestCin();
        testc.setVisible(true);
        ClientInterface inscclient = new ClientInterface();
        testc.verifier.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                String ciin = testc.cin.getText();
                int cin = Integer.parseInt(ciin);
                Boolean tcin = agence.test_cin(cin);
                if ( tcin == true ){
                    inscclient.setVisible(true);
                    inscclient.cin.setText(ciin);
                    inscclient.valider.addActionListener(new ActionListener() {
                        @Override
                        public void actionPerformed(ActionEvent ae) {
                          String name_1 = inscclient.name.getText();
                          String prenom_1 = inscclient.prenom.getText();
                          String numtelf_1 = inscclient.num_tlf.getText();
                          int num = Integer.parseInt(numtelf_1);
                          agence.ajout_client(name_1, prenom_1, num, num);
                        }
                    });
                }else {
                    System.out.println("votre client existe déjà ! ");
                    
                }
            }
        });
        
        
                
