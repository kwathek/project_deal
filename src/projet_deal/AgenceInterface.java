    private void ajoutHotelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajoutHotelActionPerformed
         AjoutHotel ajouthotel = new AjoutHotel();
        
         JComboBox<String> combo = new JComboBox<>();
         for (int i =0 ; i<agence.list_f.size(); i++){
             ajouthotel.combo.addItem(agence.list_f.get(i).getNom());
         }
          
         ajouthotel.setVisible(true);
         ajouthotel.ajouter.addActionListener(new ActionListener() {
             @Override
             public void actionPerformed(ActionEvent ae) {
                
                String desc = ajouthotel.description.getText();
                String prix_ii = ajouthotel.prix_i.getText();
                double prix_i = Double.parseDouble(prix_ii);
                String prix_dd = ajouthotel.prix_deal.getText();
                double prix_deal = Double.parseDouble(prix_ii);
                 java.util.Date date_debut =  ajouthotel.date_d.getDate();
                 java.util.Date date_fin = ajouthotel.date_fin.getDate();
                localisation = String.valueOf(ajouthotel.combo.getSelectedItem());
               java.util.Date date_ajout =  ajouthotel.date_ajout.getDate();
                agence.ajout_hotel(desc, prix_i, prix_deal, date_debut, date_fin, localisation, date_ajout);
             }
             
         });    
    }//GEN-LAST:event_ajoutHotelActionPerformed

    private void inscritFournisseurActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inscritFournisseurActionPerformed
         TestFournisseur testf = new TestFournisseur();
         
         testf.setVisible(true);
         testf.verifier.addActionListener(new ActionListener() {
             @Override
             public void actionPerformed(ActionEvent ae) {
                 String nom_1 = testf.nom_f.getText();
                 Boolean testn = agence.test_localisation(nom_1);
                 if (testn == true){
                     LocalisationIntreface loca = new LocalisationIntreface();
                     loca.setVisible(true);
                     loca.nom.setText(nom_1);
                     loca.valider.addActionListener(new ActionListener() {
                         @Override
                         public void actionPerformed(ActionEvent ae) {
                         String adr = loca.adress.getText();
                         String num_tlf = loca.num_telf.getText();
                         String mail = loca.email.getText();
                         String h_travail = loca.horaire.getText();
                         int num =Integer.parseInt(num_tlf);
                         agence.ajout_localisation(nom_1, adr, num, mail, h_travail);
                         }
                     });
                     
                     
                 }else{
                      System.out.println("votre client existe déjà ! ");
                 }
             }
         });
    }//GEN-LAST:event_inscritFournisseurActionPerformed

